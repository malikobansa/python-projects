#A Python Program that asks the user to enter two integers obtains them from the user
# And prints the sum, product, difference and quotient(division)

num1 = int(input("Enter first integer :"))

num2 = int(input("Enter second integer :"))

sum = num1 + num2

difference = num1 - num2

product = num1 * num2

quotient = num1 / num2

print("The sum of {num1} and {num2}:", sum)
print("The difference of {num1} and {num2} is :", difference)
print("The product of {num1} and {num2} is :", product)
print("The quotient4 of {num1} and {num2} is :", quotient)
